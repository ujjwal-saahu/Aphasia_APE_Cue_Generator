def get_category_meta_prompt(subcategory: str) -> str:
    return CATEGORY_META_PROMPTS.get(
        str(subcategory).strip(),
        """
你是一位專門為高齡失語症患者設計語言治療提示句的專家。請根據目標詞彙最具辨識度的特徵，生成一個簡短、具體、生活化且容易聯想到答案的提示句，不可直接說出目標詞。每次只輸出一個提示句，不要解釋。
""".strip()
    )


def build_category_prompt_variants(subcategory: str) -> pd.DataFrame:
    """
    Build controlled professor-style prompt variants for one category.
    Remove overly short variant to avoid weak generic prompts winning.
    """
    base_meta = get_category_meta_prompt(subcategory)

    variants = [
        {
            "prompt_variant": "v1_meta_base",
            "prompt_text": base_meta
        },
        {
            "prompt_variant": "v2_discriminative",
            "prompt_text": (
                base_meta
                + "\n\n補充要求：請優先選擇最有辨識度、最能區分其他同類詞彙的特徵，避免過於廣泛的描述。"
            )
        },
        {
            "prompt_variant": "v3_life_context",
            "prompt_text": (
                base_meta
                + "\n\n補充要求：請優先使用高齡者熟悉的生活經驗、家庭情境或日常場景來設計提示句。"
            )
        },
    ]

    df = pd.DataFrame(variants)
    df["subcategory"] = subcategory
    df["prompt_id"] = [f"{subcategory}_p{i+1:02d}" for i in range(len(df))]
    df["prompt_family"] = "category_meta"

    return df[["subcategory", "prompt_id", "prompt_family", "prompt_variant", "prompt_text"]]


def build_all_category_prompt_candidates(subcategories: list) -> pd.DataFrame:
    all_rows = []
    for subcat in sorted(subcategories):
        all_rows.append(build_category_prompt_variants(subcat))
    return pd.concat(all_rows, ignore_index=True)