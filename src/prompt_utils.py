import pandas as pd


def build_seed_examples(best_human_df: pd.DataFrame, top_k_per_subcategory: int = 5) -> pd.DataFrame:
    seed_rows = []

    for subcat, group in best_human_df.groupby("subcategory"):
        top_group = group.sort_values(
            ["score_mean", "cue_length_chars"],
            ascending=[False, True]
        ).head(top_k_per_subcategory)
        seed_rows.append(top_group)

    seed_df = pd.concat(seed_rows, ignore_index=True)
    return seed_df


def sample_demo_examples(
    seed_df: pd.DataFrame,
    subcategory: str = None,
    n_examples: int = 3,
    random_state: int = 42
):
    if subcategory is not None:
        same_subcat = seed_df[seed_df["subcategory"] == subcategory]
        if len(same_subcat) >= n_examples:
            return same_subcat.sample(n=n_examples, random_state=random_state).to_dict("records")

    if len(seed_df) >= n_examples:
        return seed_df.sample(n=n_examples, random_state=random_state).to_dict("records")

    return seed_df.to_dict("records")


def format_demo_block(demo_examples):
    lines = []
    for ex in demo_examples:
        lines.append(f"目標詞彙：{ex['target_word']}")
        lines.append(f"提示：{ex['cue_text']}")
        lines.append("")
    return "\n".join(lines).strip()


